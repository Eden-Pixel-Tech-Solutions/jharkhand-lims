// Quick lookup map for ICD-10 code to description
export const ICD10_MAP = Object.fromEntries(ICD10_CODES.map(x => [x.code, x.description]));

// Search function
export function searchICD10(query) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  return ICD10_CODES.filter(
    x => x.code.toLowerCase().startsWith(q) || x.description.toLowerCase().includes(q)
  ).slice(0, 12);
}
