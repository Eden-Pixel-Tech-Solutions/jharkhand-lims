// Get diet plan suggestions based on diagnosis text or ICD-10 codes
export function suggestDietPlans(diagnosisText = '', icd10Codes = []) {
  const text = diagnosisText.toLowerCase();
  const codes = icd10Codes.map(c => c.code);
  return DIET_PLANS.filter(plan =>
    plan.forDiagnoses.some(d =>
      text.includes(d.toLowerCase()) || codes.some(c => c.startsWith(d))
    )
  );
}
